    const nameInput = document.querySelector(".nameInput");
    const textArea = document.querySelector(".commTextArea");
    const intupDiv = document.querySelector(".intupDiv");
    const divWithComm = document.querySelector(".divWithComm");

    btn.onclick = () => {
        if (nameInput.value === "" || textArea.value === "") {
        } else {
            const nameValue = nameInput.value;
            const textValeu = textArea.value;
            nameInput.value = '';
            textArea.value = '';
            let wrapper = document.createElement('div');
            wrapper.classList.add("commentOne");
            divWithComm.appendChild(wrapper);
            let imgAv = document.createElement("IMG");
            imgAv.classList.add("userPick");
            imgAv.setAttribute("src", './img/user.png')
            wrapper.appendChild(imgAv);
            let userNiqName = document.createElement("h5");
            userNiqName.classList.add("userNiq");
            userNiqName.innerHTML = nameValue;
            wrapper.appendChild(userNiqName);
            let userTextComment = document.createElement("p");
            userTextComment.classList.add("userComm");
            userTextComment.innerHTML = textValeu;
            wrapper.appendChild(userTextComment);
            textArea.addEventListener("keyup", function (event) {
                if (event.keyCode === 13) {
                    event.preventDefault();
                    document.querySelector(".submitButton").click();
                }
            });
        }
    }
