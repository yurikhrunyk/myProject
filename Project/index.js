{
    const btnPlus = document.querySelector(".btnClassStylePlus");
    const btnMinus = document.querySelector(".btnClassStyleMinus");
    const pricePerDay = document.querySelector(".prcPerDay").innerHTML;
    const daysValue = document.querySelector(".calculator").innerHTML;
    const pricePerDayTotal = document.querySelector(".calculatorTotal").innerHTML;

    let daysValueNum = parseInt(daysValue, Number);
    let pricePerDayNum = parseInt(pricePerDay, Number);
    let pricePerDayNumStat = pricePerDayNum;
    let pricePerDayTotalNum = parseInt(pricePerDayTotal, Number);
    console.log(pricePerDay);

    btnPlus.onclick = () => {
        daysValueNum = daysValueNum + 1;
        let n = daysValueNum.toString();
        pricePerDayTotalNum = pricePerDayTotalNum + pricePerDayNumStat;
        let b = pricePerDayTotalNum.toString();
        document.querySelector(".calculator").innerHTML = n;
        document.querySelector(".calculatorTotal").innerHTML = b;
    }
    console.log(pricePerDayTotal);
    btnMinus.onclick = () => {
        if (pricePerDayTotalNum > 0) {
            daysValueNum = daysValueNum - 1;
            let n = daysValueNum.toString();
            pricePerDayTotalNum = pricePerDayTotalNum - pricePerDayNumStat;
            let b = pricePerDayTotalNum.toString();
            document.querySelector(".calculator").innerHTML = n;
            document.querySelector(".calculatorTotal").innerHTML = b;
        }

    }
}
